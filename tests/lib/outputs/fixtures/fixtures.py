        resource_type="test-resource",
        resource_uid=resource_uid,
        resource_name=resource_name,
        resource_details=resource_details,
        resource_tags=resource_tags,
        partition=provider,
        region=region,
        description="check description",
        risk="test-risk",
        related_url="test-url",
